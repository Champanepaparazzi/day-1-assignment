// Declare the numbers using var
var num1 = 5;
var num2 = 10;
var num3 = 15;
var num4 = 20;
var num5 = 25;

// Add the numbers
var sum = num1 + num2 + num3 + num4 + num5;
console.log("The sum of the numbers is: " + sum);

// Subtract 40 from the sum
var sub = sum - 40;
console.log("The difference of the numbers is: " + sub);

// Multiply the result by 30
var result = sub * 30;
console.log("The multiplication of the result is: " + result);
